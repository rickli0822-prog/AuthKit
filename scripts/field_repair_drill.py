"""Run a non-mutating repair audit handoff drill.

The drill exercises the low-risk DNS repair audit path without flushing the
developer machine DNS cache. It patches the command runner in-process, redirects
AuthKit's audit location to a temporary directory, writes a support bundle, and
validates that bundle.
"""

from __future__ import annotations

import os
import sys
import tempfile
from pathlib import Path

from authkit import __version__
from authkit.cli import _validate_support_bundle, _write_support_bundle
from authkit.models import DiagnosisReport, FailureCase, HealthStatus, LayerResult
from authkit.repair import fixer
from authkit.repair.audit import default_audit_path, load_audit_records


def main() -> int:
    original_env = {name: os.environ.get(name) for name in ("LOCALAPPDATA", "APPDATA", "USERPROFILE")}
    original_flush_dns_cache = fixer.flush_dns_cache
    with tempfile.TemporaryDirectory(prefix="authkit-field-repair-drill-") as tmp_name:
        tmp = Path(tmp_name)
        os.environ["LOCALAPPDATA"] = str(tmp / "LocalAppData")
        os.environ["APPDATA"] = str(tmp / "AppData")
        os.environ["USERPROFILE"] = str(tmp / "UserProfile")
        fixer.flush_dns_cache = _fake_flush_dns_cache
        try:
            message = fixer.apply_direct_repair("flush-dns-cache", client="manual")
            records = load_audit_records()
            if len(records) != 1:
                raise RuntimeError(f"expected one repair audit record, found {len(records)}")
            record = records[0]
            _assert(record.fix_id == "flush-dns-cache", "unexpected fix_id")
            _assert(record.status == "success", "repair audit did not record success")
            _assert(record.changed_keys == ["DnsClientCache"], "unexpected changed keys")
            _assert(record.rollback_supported is False, "DNS flush must remain non-rollbackable")
            _assert(record.risk == "low", "DNS flush must remain low risk")
            _assert(record.admin_required is False, "DNS flush must not require admin")
            _assert(record.before.get("dns_cache", {}).get("persistent_config_changed") is False, "before snapshot changed")
            _assert("Simulated DNS cache flush" in "\n".join(record.warnings), "simulated command output missing")

            bundle_path = tmp / "authkit-support-bundle.repair-drill.json"
            _write_support_bundle(
                client="codex",
                out_path=bundle_path,
                audit_limit=5,
                locale="en",
                fast=True,
            )
            problems = _validate_support_bundle(bundle_path)
            if problems:
                raise RuntimeError("support bundle validation failed: " + "; ".join(problems))

            print(f"repair-message: {message}")
            print(f"audit-records: {len(records)}")
            print(f"audit-path: {default_audit_path()}")
            print(f"bundle-ok: {bundle_path.name}")
            print("AuthKit field repair drill passed.")
            return 0
        finally:
            fixer.flush_dns_cache = original_flush_dns_cache
            for name, value in original_env.items():
                if value is None:
                    os.environ.pop(name, None)
                else:
                    os.environ[name] = value


def _fake_flush_dns_cache() -> dict[str, object]:
    return {
        "command": "ipconfig /flushdns",
        "returncode": 0,
        "stdout": "Simulated DNS cache flush for AuthKit field repair drill.",
        "stderr": "",
    }


def _fake_diagnosis(*, client: str, locale: str, fast: bool = False) -> DiagnosisReport:
    return DiagnosisReport(
        tool_version=__version__,
        platform="Windows",
        client=client,
        status=HealthStatus.HEALTHY,
        case=FailureCase.NONE,
        root_cause="Field repair drill diagnosis stub.",
        confidence="drill",
        browser_explanation="Generated by non-mutating AuthKit field repair drill.",
        layers=[
            LayerResult(
                name="repair_drill",
                ok=True,
                summary=f"locale={locale}; fast={fast}",
                details={"network_mutation": False},
            )
        ],
        notes=["field_repair_drill=true"],
    )


def _assert(condition: bool, message: str) -> None:
    if not condition:
        raise RuntimeError(message)


if __name__ == "__main__":
    try:
        import authkit.cli as cli

        cli.run_diagnosis = _fake_diagnosis
        raise SystemExit(main())
    except RuntimeError as exc:
        print(f"field repair drill failed: {exc}", file=sys.stderr)
        raise SystemExit(1)
